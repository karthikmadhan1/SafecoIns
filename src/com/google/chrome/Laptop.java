package com.google.chrome;

public class Laptop {
	
	public void runAProgram() {
		System.out.println("Run a program in laptop ");
	}
	
	public void playGames() {
		System.out.println("Play games in laptop");
	}
	
	public void watchMovies() {
		System.out.println("Watch movies in the laptop");
	}
	
	public static void main(String[]args) {
		
		Laptop siddhu=new Laptop();
		siddhu.runAProgram();
		siddhu.playGames();
		siddhu.watchMovies();
		
	
	}
}
